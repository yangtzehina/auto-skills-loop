            PlannedFile(path='SKILL.md', purpose='entry', source_basis=[]),
            PlannedFile(path='scripts/run.py', purpose='script', source_basis=[]),
            PlannedFile(path='scripts/keep.py', purpose='script', source_basis=[]),
