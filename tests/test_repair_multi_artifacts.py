from __future__ import annotations

import json

from openclaw_skill_create.models.artifacts import ArtifactFile, Artifacts
from openclaw_skill_create.models.diagnostics import Diagnostics, ValidationResult
from openclaw_skill_create.models.plan import PlannedFile, SkillPlan
from openclaw_skill_create.models.request import SkillCreateRequestV6
from openclaw_skill_create.services.repair import run_repair


def test_run_repair_fills_empty_reference_and_script_files():
    artifacts = Artifacts(
        files=[
            ArtifactFile(path='SKILL.md', content='---\nname: demo\ndescription: demo\n---\n', content_type='text/markdown'),
            ArtifactFile(path='references/usage.md', content=''),
            ArtifactFile(path='scripts/run.py', content=''),
        ]
    )
    diagnostics = Diagnostics(
        validation=ValidationResult(
            frontmatter_valid=True,
            skill_md_within_budget=True,
            planned_files_present=True,
            unnecessary_files_present=False,
            unreferenced_reference_files=[],
            unsupported_claims_found=False,
            repairable_issue_types=['empty_reference_file', 'empty_script_file'],
            summary=['Empty reference file: references/usage.md', 'Empty script file: scripts/run.py'],
        )
    )
    skill_plan = SkillPlan(
        skill_name='demo',
        files_to_create=[
            PlannedFile(path='SKILL.md', purpose='entry', source_basis=[]),
            PlannedFile(path='references/usage.md', purpose='reference', source_basis=[]),
            PlannedFile(path='scripts/run.py', purpose='script', source_basis=[]),
        ],
    )

    result = run_repair(
        artifacts=artifacts,
        diagnostics=diagnostics,
        skill_plan=skill_plan,
        skill_name='demo',
        description='Repo-aware skill for demo',
        max_skill_md_lines=300,
    )

    refs = next(file for file in result.repaired_artifacts.files if file.path == 'references/usage.md')
    script = next(file for file in result.repaired_artifacts.files if file.path == 'scripts/run.py')
    assert result.applied is True
    assert '## Overview' in refs.content
    assert '## Key points' in refs.content
    assert '- Source file: `references/usage.md`' in refs.content
    assert 'def main() -> int:' in script.content
    assert 'raise NotImplementedError("Implement run.py.")' in script.content
    assert refs.status == 'repaired'
    assert script.status == 'repaired'


