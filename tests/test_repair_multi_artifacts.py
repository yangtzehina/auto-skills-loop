def test_run_repair_rebuilds_invalid_eval_scaffold_files():
    artifacts = Artifacts(
        files=[
            ArtifactFile(path='SKILL.md', content='---\nname: demo\ndescription: demo\n---\n', content_type='text/markdown'),
            ArtifactFile(path='evals/trigger_eval.json', content='{}', content_type='application/json'),
            ArtifactFile(path='evals/output_eval.json', content='', content_type='application/json'),
            ArtifactFile(path='evals/benchmark.json', content='{"skill_name":"demo","dimensions":[]}\n', content_type='application/json'),
        ]
    )
    diagnostics = Diagnostics(
        validation=ValidationResult(
            frontmatter_valid=True,
            skill_md_within_budget=True,
            planned_files_present=True,
            unnecessary_files_present=False,
            unreferenced_reference_files=[],
            unsupported_claims_found=False,
            repairable_issue_types=['invalid_eval_scaffold', 'empty_eval_scaffold'],
            summary=[
                'Invalid eval scaffold: evals/trigger_eval.json',
                'Empty eval scaffold file: evals/output_eval.json',
            ],
        )
    )
    skill_plan = SkillPlan(
        skill_name='demo',
        files_to_create=[
            PlannedFile(path='SKILL.md', purpose='entry', source_basis=[]),
            PlannedFile(path='evals/trigger_eval.json', purpose='trigger eval', source_basis=[]),
            PlannedFile(path='evals/output_eval.json', purpose='output eval', source_basis=[]),
            PlannedFile(path='evals/benchmark.json', purpose='benchmark eval', source_basis=[]),
        ],
    )

    result = run_repair(
        artifacts=artifacts,
        diagnostics=diagnostics,
        skill_plan=skill_plan,
        skill_name='demo',
        description='Repo-aware skill for demo',
        max_skill_md_lines=300,
        request=SkillCreateRequestV6(task='Capture notes into Notion', enable_eval_scaffold=True),
    )

    trigger = next(file for file in result.repaired_artifacts.files if file.path == 'evals/trigger_eval.json')
    output = next(file for file in result.repaired_artifacts.files if file.path == 'evals/output_eval.json')
    benchmark = next(file for file in result.repaired_artifacts.files if file.path == 'evals/benchmark.json')

    assert result.applied is True
    assert json.loads(trigger.content)['cases']
    assert json.loads(output.content)['cases']
    assert json.loads(benchmark.content)['dimensions']
    assert trigger.status == 'repaired'
    assert output.status == 'repaired'
