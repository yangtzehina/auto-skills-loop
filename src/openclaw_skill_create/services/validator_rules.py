def _validate_operation_contract(
    *,
    skill_plan: SkillPlan,
    artifacts: Artifacts,
    skill_md_content: Optional[str],
    validation: ValidationResult,
) -> list[str]:
    skill_archetype = str(getattr(skill_plan, 'skill_archetype', 'guidance') or 'guidance').strip().lower()
    if skill_archetype != 'operation_backed':
        return []

    notes: list[str] = []
    operation_contract = getattr(skill_plan, 'operation_contract', None)
    if operation_contract is None:
        validation.summary.append('Operation contract missing: skill plan did not carry an operation-backed contract.')
        return notes

    contract_content = find_file_content(artifacts, OPERATION_CONTRACT_PATH)
    if not contract_content:
        validation.summary.append(f'Operation contract missing: expected `{OPERATION_CONTRACT_PATH}` artifact.')
        return notes

    try:
        contract_payload = json.loads(contract_content)
    except Exception:
        validation.summary.append(f'Operation contract invalid: `{OPERATION_CONTRACT_PATH}` is not valid JSON.')
        return notes

    operations = list(getattr(operation_contract, 'operations', []) or [])
    operation_names = [
        getattr(item, 'name', '')
        for group in operations
        for item in list(getattr(group, 'operations', []) or [])
        if getattr(item, 'name', '')
    ]
    if not operations or not operation_names:
        validation.summary.append('Operation surface incomplete: operation-backed skill has no operation groups or operation specs.')

    missing_mentions = [
        name for name in operation_names[:5]
        if skill_md_content is not None and f'`{name}`' not in skill_md_content and name not in skill_md_content
    ]
    if missing_mentions:
        validation.summary.append(
            'Operation surface incomplete: SKILL.md does not mention operation names '
            + ', '.join(missing_mentions[:3])
        )

    supports_json = bool(getattr(operation_contract, 'supports_json', False))
    if supports_json:
        has_json_surface = False
        if skill_md_content and any(marker in skill_md_content.lower() for marker in JSON_DOCUMENTATION_PATTERNS):
            has_json_surface = True
        if not has_json_surface:
            for file in artifacts.files:
                if not file.path.startswith('scripts/'):
                    continue
                lowered = (file.content or '').lower()
                if any(marker in lowered for marker in JSON_DOCUMENTATION_PATTERNS) or 'json.dumps' in lowered or 'json.loads' in lowered:
                    has_json_surface = True
                    break
        if not has_json_surface:
            validation.summary.append('Operation JSON support mismatch: contract expects JSON support but generated skill surface does not document it.')

    mutability = str(getattr(operation_contract, 'mutability', 'read_only') or 'read_only').strip().lower()
    if mutability in {'mixed', 'mutating'}:
        weak_operations = [
            getattr(item, 'name', '')
            for group in operations
            for item in list(getattr(group, 'operations', []) or [])
            if not list(getattr(item, 'side_effects', []) or []) or not list(getattr(item, 'preconditions', []) or [])
        ]
        if weak_operations:
            validation.summary.append(
                'Operation mutating safeguards incomplete: '
                + ', '.join(weak_operations[:3])
                + ' is missing preconditions or side-effect notes.'
            )

    session_model = str(getattr(operation_contract, 'session_model', 'stateless') or 'stateless').strip().lower()
    if session_model == 'session_required':
        if not skill_md_content or 'session' not in skill_md_content.lower():
            validation.summary.append('Operation session model missing from SKILL.md: contract requires a session-aware workflow.')

    if contract_payload.get('backend_kind'):
        notes.append(
            'Validator operation diagnostics: '
            f"backend_kind={contract_payload.get('backend_kind')}; "
            f"supports_json={bool(contract_payload.get('supports_json', False))}; "
            f"session_model={contract_payload.get('session_model', 'stateless')}; "
            f"mutability={contract_payload.get('mutability', 'read_only')}"
        )
    if find_file_content(artifacts, OPERATION_VALIDATION_PATH):
        notes.append(f'Validator operation diagnostics: operation_validation_artifact={OPERATION_VALIDATION_PATH}')
    coverage_content = find_file_content(artifacts, OPERATION_COVERAGE_PATH)
    if not coverage_content:
        validation.summary.append(f'Operation coverage missing: expected `{OPERATION_COVERAGE_PATH}` artifact.')
        return notes
    try:
        coverage_payload = json.loads(coverage_content)
    except Exception:
        validation.summary.append(f'Operation coverage invalid: `{OPERATION_COVERAGE_PATH}` is not valid JSON.')
        return notes
    if not isinstance(coverage_payload, dict):
        validation.summary.append(f'Operation coverage invalid: `{OPERATION_COVERAGE_PATH}` must be a JSON object.')
        return notes
    covered_operations = [
        str(item or '').strip()
        for item in list(coverage_payload.get('covered_operations') or [])
        if str(item or '').strip()
    ]
    missing_operations = [
        str(item or '').strip()
        for item in list(coverage_payload.get('missing_operations') or [])
        if str(item or '').strip()
    ]
    if sorted(set(covered_operations + missing_operations)) != sorted(set(operation_names)):
        validation.summary.append(
            'Operation coverage invalid: operation_coverage.json must account for every contract operation '
            f'({len(operation_names)} declared, {len(set(covered_operations + missing_operations))} tracked).'
        )
    recommended_followup = str(coverage_payload.get('recommended_followup', 'no_change') or 'no_change').strip().lower()
    if not missing_operations and recommended_followup not in {'no_change', 'patch_current'} and operation_names:
        validation.summary.append(
            f'Operation coverage follow-up mismatch: recommended_followup=`{recommended_followup}` is inconsistent with a fully covered operation surface.'
        )
    notes.append(
        'Validator operation diagnostics: '
        f'operation_coverage_artifact={OPERATION_COVERAGE_PATH}; '
        f'recommended_followup={recommended_followup}; '
        f'missing_operations={missing_operations[:3]}'
    )
    return notes
