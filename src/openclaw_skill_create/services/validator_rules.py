def _script_non_empty_lines(content: str) -> list[str]:
    return [line.strip() for line in content.splitlines() if line.strip()]


def _script_code_signal_count(path: str, content: str) -> int:
    score = 0
    stripped = content.strip()
    if not stripped:
        return 0
    if stripped.startswith('#!'):
        score += 2
    lower = stripped.lower()
    for token in SCRIPT_STRONG_TOKENS:
        if token in stripped:
            score += 1
    suffix = Path(path).suffix.lower()
    if suffix == '.py':
        try:
            compile(content, path, 'exec')
            score += 2
        except SyntaxError:
            pass
        except Exception:
            score += 1
    elif suffix in SCRIPT_SUFFIX_HINTS:
        if any(token in stripped for token in ('=', '(', ')', '{', '}', '$', ';')):
            score += 1
    if lower.startswith('python ') or lower.startswith('bash '):
        score -= 1
    return score


def validate_script_placeholder_heavy(artifacts: Artifacts) -> list[str]:
    issues = []
    for file in artifacts.files:
        if not file.path.startswith('scripts/'):
            continue
        content = file.content or ''
        stripped = content.strip()
        if not stripped:
            continue
        lower = stripped.lower()
        non_empty_lines = _script_non_empty_lines(content)
        placeholder_line_count = sum(
            1 for line in non_empty_lines if any(marker in line.lower() for marker in SCRIPT_PLACEHOLDER_MARKERS)
        )
        if placeholder_line_count == 0:
            continue
        if placeholder_line_count >= max(1, len(non_empty_lines) - 1):
            issues.append(file.path)
            continue
        if _script_code_signal_count(file.path, content) <= 1:
            issues.append(file.path)
    return issues


def validate_script_non_code_like(artifacts: Artifacts) -> list[str]:
    issues = []
    for file in artifacts.files:
        if not file.path.startswith('scripts/'):
            continue
        content = file.content or ''
        stripped = content.strip()
        if not stripped:
            continue
        lower = stripped.lower()
        non_empty_lines = _script_non_empty_lines(content)
        code_signals = _script_code_signal_count(file.path, content)
        placeholder_markers = sum(1 for marker in SCRIPT_PLACEHOLDER_MARKERS if marker in lower)
        comment_like_lines = sum(
            1
            for line in non_empty_lines
            if line.startswith('#') or line.startswith('//') or line.startswith('/*') or line.startswith('*')
        )
        markdown_like_lines = sum(
            1
            for line in non_empty_lines[:8]
            if line.startswith('# ')
            or line.startswith('## ')
            or line.startswith('- ')
            or line.startswith('* ')
            or line[:2].isdigit() and line[1:3] == '. '
        )
        wrapper_markers = sum(1 for marker in SCRIPT_WRAPPER_MARKERS if marker in lower)
        if placeholder_markers > 0 and (code_signals <= 1 or comment_like_lines == len(non_empty_lines)):
            issues.append(file.path)
            continue
        if wrapper_markers > 0 and code_signals <= 1:
            issues.append(file.path)
            continue
        if markdown_like_lines >= max(2, len(non_empty_lines[:8]) // 2) and code_signals == 0:
            issues.append(file.path)
    return issues


def validate_script_wrapper_like(artifacts: Artifacts) -> list[str]:
    issues = []
    for path in validate_script_non_code_like(artifacts):
        content = find_file_content(artifacts, path) or ''
        lower = content.lower()
        if any(marker in lower for marker in SCRIPT_WRAPPER_MARKERS) or '```' in content:
            issues.append(path)
    return issues


def _iter_patterns(extracted_patterns: Any) -> list[Any]:
    if extracted_patterns is None:
        return []
    patterns = getattr(extracted_patterns, 'patterns', None)
    if patterns is not None:
        return list(patterns)
    if isinstance(extracted_patterns, dict):
        return list(extracted_patterns.get('patterns', []) or [])
    return []


def _get_attr(obj: Any, key: str, default=None):
    if hasattr(obj, key):
        return getattr(obj, key)
    if isinstance(obj, dict):
        return obj.get(key, default)
    return default


def collect_pattern_validator_checks(extracted_patterns: Any) -> list[str]:
    checks: list[str] = []
    for pattern in _iter_patterns(extracted_patterns):
        status = (_get_attr(pattern, 'status', 'candidate') or 'candidate').lower()
        if status not in {'accepted', 'candidate'}:
            continue
        downstream_hints = _get_attr(pattern, 'downstream_hints', None)
        for check in list(_get_attr(downstream_hints, 'validator_checks', []) or []):
            if check not in checks:
                checks.append(check)
    return checks


def run_pattern_validator_checks(
    *,
    extracted_patterns: Any,
    skill_md_content: Optional[str],
    validation: ValidationResult,
    existing_issue_paths: dict[str, list[str]],
) -> tuple[list[str], list[str]]:
    requested_checks = collect_pattern_validator_checks(extracted_patterns)
    if not requested_checks:
        return [], []

    summary: list[str] = []
    notes: list[str] = []
    frontmatter = _parse_frontmatter(skill_md_content)
    description = frontmatter.get('description', '')

    for check in requested_checks:
        if check == PATTERN_CHECK_DESCRIPTION:
            if not _description_has_capability_and_trigger(description):
                summary.append('Pattern check failed: description should mention both capability and trigger')
            else:
                notes.append('Pattern-aware validator check passed: description_mentions_capability_and_trigger')
        elif check == PATTERN_CHECK_REFERENCE_LINKED:
            if validation.unreferenced_reference_files:
                summary.append('Pattern check failed: reference files should be linked from SKILL.md')
            else:
                notes.append('Pattern-aware validator check passed: reference_files_are_linked_from_skill_md')
        elif check == PATTERN_CHECK_REFERENCE_NOT_PLACEHOLDER:
            if existing_issue_paths.get('reference_placeholder'):
                summary.append('Pattern check failed: reference files should not remain placeholder-heavy')
            else:
                notes.append('Pattern-aware validator check passed: reference_file_is_not_placeholder_heavy')
        else:
            notes.append(f'Pattern-aware validator check skipped (unsupported): {check}')

    if requested_checks:
        notes.append(f'Pattern-aware validator checks requested: {requested_checks}')
    return summary, notes


