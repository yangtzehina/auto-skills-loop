def validate_invalid_eval_scaffold(artifacts: Artifacts) -> list[str]:
    issues = []
    for file in artifacts.files:
        if not (file.path.startswith('evals/') and file.path.endswith('.json')):
            continue

        content = file.content or ''
        if not content.strip():
            continue

        try:
            payload = json.loads(content)
        except Exception:
            issues.append(file.path)
            continue

        if not isinstance(payload, dict) or not payload.get('skill_name'):
            issues.append(file.path)
            continue

        if file.path.endswith('trigger_eval.json') and not isinstance(payload.get('cases'), list):
            issues.append(file.path)
            continue
        if file.path.endswith('output_eval.json') and not isinstance(payload.get('cases'), list):
            issues.append(file.path)
            continue
        if file.path.endswith('benchmark.json') and not isinstance(payload.get('dimensions'), list):
            issues.append(file.path)
            continue

    return issues


