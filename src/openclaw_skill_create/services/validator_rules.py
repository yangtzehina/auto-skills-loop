def validate_skill_md_frontmatter(content: Optional[str]) -> bool:
    if not content or not content.startswith('---\n'):
        return False

    parts = content.split('---\n', 2)
    if len(parts) < 3:
        return False

    raw = parts[1].strip().splitlines()
    keys = []
    for line in raw:
        if ':' not in line:
            return False
        keys.append(line.split(':', 1)[0].strip())

    return sorted(keys) == ['description', 'name']


def validate_skill_md_budget(content: Optional[str], max_lines: int) -> bool:
    if not content:
        return False
    return len(content.splitlines()) <= max_lines


def _parse_frontmatter(content: Optional[str]) -> dict[str, str]:
    if not content or not content.startswith('---\n'):
        return {}
    parts = content.split('---\n', 2)
    if len(parts) < 3:
        return {}
    frontmatter = {}
    for line in parts[1].strip().splitlines():
        if ':' not in line:
            continue
        key, value = line.split(':', 1)
        frontmatter[key.strip()] = value.strip()
    return frontmatter


def _description_has_capability_and_trigger(description: str) -> bool:
    if not description:
        return False

    lowered = description.lower()
    trigger_markers = ['use when', 'used when', 'when codex needs', 'when the task', 'when you need', '当', '用于']
    trigger_idx = -1
    matched_marker = ''
    for marker in trigger_markers:
        idx = lowered.find(marker)
        if idx != -1:
            trigger_idx = idx
            matched_marker = marker
            break

    if trigger_idx == -1:
        return False

    capability_part = description[:trigger_idx].strip(' ,;:-')
    trigger_part = description[trigger_idx + len(matched_marker):].strip(' ,;:-')
    if not capability_part or len(capability_part) < 8:
        return False
    if not trigger_part or len(trigger_part) < 8:
        return False

    capability_markers = [
        'create', 'refine', 'audit', 'edit', 'analyze', 'generate', 'manage',
        'build', 'review', 'restructure', '查询', '创建', '管理', '审计', '更新', '分析'
    ]
    return any(token in capability_part.lower() for token in capability_markers) or len(capability_part.split()) >= 3


def validate_unreferenced_reference_files(
    artifacts: Artifacts,
    skill_md_content: Optional[str],
) -> list[str]:
    if not skill_md_content:
        return [f.path for f in artifacts.files if f.path.startswith('references/') and f.path != OPERATION_CONTRACT_PATH]

    missing = []
    for file in artifacts.files:
        if file.path == OPERATION_CONTRACT_PATH:
            continue
        if file.path.startswith('references/') and f'`{file.path}`' not in skill_md_content:
            missing.append(file.path)
    return missing


def validate_unsupported_claims(content: Optional[str]) -> bool:
    if not content:
        return False

    suspicious_markers = [
        'production-ready',
        'fully automated',
        'guaranteed',
        'supports everything',
        'complete end-to-end solution',
    ]
    lowered = content.lower()
    return any(marker in lowered for marker in suspicious_markers)


